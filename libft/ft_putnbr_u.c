#include "libft.h"

void	putnbr_u(unsigned int nb)
{
	char	nb_p;

	if (nb > 9)
	{
		nb_p = (nb % 10) + 48;
		ft_putnbr(nb / 10);
		write(1, &nb_p, 1);
	}
	else
	{
		nb_p = nb + 48;
		write(1, &nb_p, 1);
	}
}
