#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
	size_t	i;
	char	*s_bis;

	i = -1;
	s_bis = s;
	while (++i < n)
	{
		s_bis[i] = c;
	}
	return (s);
}
